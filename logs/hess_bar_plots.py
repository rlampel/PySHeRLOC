# Settings
bar_height = 0.8
colors = {4: 'red', 3: 'orange', 2: 'yellow', 1: 'yellowgreen', 0: 'green', -1: 'black'}

nrows = 5
ncols = 7

fig, ax = plt.subplots(nrows=nrows, ncols=ncols)


def plot_data(data, curr_ax):
    for i, row in enumerate(data):
        y = i

        for j, val in enumerate(row):
            curr_ax.barh(
                y=y, width=1, left=j,
                color=colors[val], edgecolor='black'
            )


num_datasets = len(data_list)
for k in range(min(num_datasets, nrows * ncols)):
    row, col = k % nrows, k // nrows
    curr_data = data_list[k].hess_list
    plot_data(curr_data, ax[row, col])
    ax[row, col].set_yticks(range(len(curr_data)))
    # ax[row, col].set_yticklabels([f"Row {i + 1}" for i in range(len(curr_data))])
    ax[row, col].set_title(data_list[k].name)
    ax[row, col].invert_yaxis()  # optional: keep first row on top
    ax[row, col].set_xlim(0, max(len(r) for r in curr_data))
    ax[row, col].set_aspect('auto')
    if row == nrows - 1:
        ax[row, col].set_xlabel("Iteration")

# plt.tight_layout()
plt.show()

